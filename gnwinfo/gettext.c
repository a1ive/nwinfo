// https://learn.microsoft.com/en-us/openspecs/office_standards/ms-oe376/6c085406-a698-4e12-9d4d-c3b0ee3dbc4a
static LANGID static_lang_id;

const char*
N_(GETTEXT_STR_ID id)
{
	if (static_lang_id == 0)
		static_lang_id = GetUserDefaultUILanguage();
	if (id >= N__MAX_)
		return INVALID_N_ID;

	const char* str = NULL;
	switch (static_lang_id)
	{
	case 2052: // Chinese - People's Republic of China
		str = lang_zh_cn[id];
		break;
	case 1028: // Chinese - Taiwan
		str = lang_zh_tw[id];
		break;
	case 1040: // Italian
		str = lang_it_it[id];
		break;
	case 1041: // Japanese
		str = lang_ja_jp[id];
		break;
	case 1042: // Korean
		str = lang_ko_kr[id];
		break;
	case 1053: // Swedish
		str = lang_sv_se[id];
		break;
	case 1055: // Turkish
		str = lang_tr_tr[id];
		break;
	}
	// 1033: English - United States
	if (str == NULL || str[0] == '\0')
		str = lang_en_us[id];
	return str;
}
